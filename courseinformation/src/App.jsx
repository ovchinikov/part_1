const Total = ({ parts }) => {
  return (
    <p>
      Number of exercises{" "}
      {parts.reduce((acc, curr) => {
        return acc + curr.exercises;
      }, 0)}
    </p>
  );
};

const Course = ({ course }) => {
  return (
    <>
      {course.map((course) => {
        return (
          <div key={course.id}>
            <Header course={course.name} />
            <Content parts={course.parts} />
            <Total parts={course.parts} />
          </div>
        );
      })}
    </>
  );
};

